public class SpellTemplate {
    public String title;
    public String description;
    public int damage;
    public int heal;
    public int deckAmount;

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }

    public int getDamage() {
        return damage;
    }

    public int getHeal() {
        return heal;
    }

    public int getDeckAmount() {
        return deckAmount;
    }
}
